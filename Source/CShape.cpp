#include "CShape.h"
#include "CMaterial.h"

namespace glliba
{
	////////////////////////////////////////////////////////////////////////////////////////////////////////////

	CShape::CShape( CNode* _parent)
		: CNode(_parent)
		, m_eTypeShape(OST_UNKNOWN)
	{
		m_eTypeNode = TN_SHAPE;
		LOG_CONSOLE( "Initialize node " << type_node[m_eTypeNode].c_str());

		m_pMaterial = new CMaterial();
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////

	CShape::~CShape()
	{
		delete m_pMaterial;
		m_pMaterial = nullptr;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////

	CMaterial* CShape::setMaterial( CMaterial* _material )
	{
		CMaterial* oldMaterial = m_pMaterial;
		m_pMaterial = _material;

		return oldMaterial;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////

	CMaterial* CShape::getMaterial() const
	{
		return m_pMaterial;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////

} //glliba
